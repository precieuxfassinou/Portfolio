// ─── APPLY LANGUAGE ────────────────────────────────────────────────────────────
let currentLang = localStorage.getItem('pf-lang') || 'fr';

function applyLang(lang) {
  const t = i18n[lang];
  currentLang = lang;
  localStorage.setItem('pf-lang', lang);

  // Lang buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // All [data-i18n] elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else if (key === 'about_bio') {
        el.innerHTML = t[key];
      } else if (key === 'il_edu_val') {
        el.innerHTML = t[key].replace(/\n/g, '<br>');
      } else {
        el.textContent = t[key];
      }
    }
  });

  // html lang attribute
  document.documentElement.lang = lang;
}

// ─── INIT ──────────────────────────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {

  // Language switcher
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.dataset.lang));
  });
  applyLang(currentLang);

  // Smooth scroll
  document.querySelectorAll('a[href^="#"]').forEach(a => {
    a.addEventListener('click', e => {
      e.preventDefault();
      const target = document.querySelector(a.getAttribute('href'));
      if (target) target.scrollIntoView({ behavior: 'smooth' });
      // Close mobile drawer if open
      document.getElementById('mobile-drawer').classList.remove('open');
    });
  });

  // Mobile drawer
  document.getElementById('nav-open').addEventListener('click', () => {
    document.getElementById('mobile-drawer').classList.add('open');
  });
  document.getElementById('mobile-close').addEventListener('click', () => {
    document.getElementById('mobile-drawer').classList.remove('open');
  });

  // Fade-in on scroll
  const obs = new IntersectionObserver(entries => {
    entries.forEach(e => {
      if (e.isIntersecting) { e.target.classList.add('visible'); obs.unobserve(e.target); }
    });
  }, { threshold: 0.08 });
  document.querySelectorAll('.fade-in').forEach(el => obs.observe(el));
});